let kommuneEL = document.getElementById("kommune");


function populatedropdown(options, select) {
    kommuneEL.innerHTML = "<option>Velg kommune...</option>";



    // clearDropdown(select)
    for (let i = 0; i < options.length; i++) {
        var kommune = options[i];
        var el = document.createElement("option");
        el.textContent = kommune;
        select.appendChild(el);
    }

}

function clearDropdown(select) {
    for (i = 0; i < select.length; i++) {
        var option = select.options[i];
        select.remove(option); 
    }
}

